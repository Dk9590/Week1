package week1.day1;

public class SwitchCase {

	public static void main(String[] args) {
		
		String Month ="jul";
		switch(Month)
		{
		case "Jun":
			System.out.println("Given is not current month");
			break;
		case "Jul":
			System.out.println("Given is  current month");
			break;
		case "Aug":
		
			System.out.println("Given is  not current month");
			break;
		default:
		{
			System.out.println("Provide valid choices");
	
		}
		
	}
}
	
}
